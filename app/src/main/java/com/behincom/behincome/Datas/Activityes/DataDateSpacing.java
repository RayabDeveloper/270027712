package com.behincom.behincome.Datas.Activityes;

import java.util.Date;

public class DataDateSpacing {

    private Date cTime, eTime;
    private String mDate;
    private int duration;

    public String mDate() {
        return mDate;
    }
    public void mDate(String mDate) {
        this.mDate = mDate;
    }

    public int duration() {
        return duration;
    }
    public void duration(int duration) {
        this.duration = duration;
    }

    public Date cTime() {
        return cTime;
    }
    public void cTime(Date cTime) {
        this.cTime = cTime;
    }

    public Date eTime() {
        return eTime;
    }
    public void eTime(Date eTime) {
        this.eTime = eTime;
    }

}
